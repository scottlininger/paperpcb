/**
 * parsePsd.js — Pure PSD parser for PcbMaker.
 * Takes a PSD file and returns structured layer data with SVGs.
 * No DOM manipulation, no SketchUp API, no STL export.
 */
import { readPsd, initializeCanvas } from 'https://esm.sh/ag-psd@18.0.1';
import ImageTracer from 'https://esm.sh/imagetracerjs@1.2.6';

// Enable browser canvas mode so each layer gets a .canvas property.
initializeCanvas((width, height) => {
  const canvas = document.createElement('canvas');
  canvas.width = width;
  canvas.height = height;
  return canvas;
});

// -- Special folder keywords and colors --
const SPECIAL_KEYWORDS = ['board', 'copper', 'mask', 'graphics'];

const FOLDER_COLORS = {
  copper: [184, 115, 51],
  board:  [210, 180, 140],
  mask:   [200, 30, 30],
};

function isSpecialFolder(name) {
  const lower = (name || '').toLowerCase();
  return SPECIAL_KEYWORDS.some((kw) => lower.includes(kw));
}

function isGraphicsFolder(name) {
  return (name || '').toLowerCase().includes('graphics');
}

function getFolderColor(name) {
  const lower = (name || '').toLowerCase();
  for (const [kw, rgb] of Object.entries(FOLDER_COLORS)) {
    if (lower.includes(kw)) return rgb;
  }
  return null;
}

function colorizeCanvas(canvas, r, g, b) {
  const ctx = canvas.getContext('2d');
  const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
  const data = imageData.data;
  for (let i = 0; i < data.length; i += 4) {
    data[i]     = r;
    data[i + 1] = g;
    data[i + 2] = b;
    // data[i + 3] (alpha) left untouched
  }
  ctx.putImageData(imageData, 0, 0);
}

// Imagetracerjs tracing settings, chosen after 3 rounds of A/B testing across
// multiple PSD inputs. Key findings:
//   - numberofcolors:2 is essential since our composites are colorized to a
//     single solid color + transparency (only 2 colors exist).
//   - blurradius:10 smooths pixel staircasing before tracing, eliminating
//     jagged edges on curves and radii. Lower values (0-5) left visible
//     jaggies; higher values (14+) started losing fine detail.
//   - ltres/qtres:0.5 gives balanced line/curve fitting — tight enough to
//     preserve shape accuracy but loose enough to minimize segment count.
//     Tighter (0.1-0.3) produced too many segments with no visual benefit;
//     looser (1+) oversimplified curved features.
//   - pathomit:20 removes small noise paths (< 20 edge nodes) that add
//     complexity without contributing meaningful geometry.
//   - rightangleenhance preserves sharp corners common in PCB layouts.
const SVG_TRACE_OPTIONS = {
  numberofcolors: 2,
  ltres: 0.5,
  qtres: 0.5,
  pathomit: 20,
  blurradius: 10,
  blurdelta: 40,
  rightangleenhance: true,
};

// -- Raster to SVG conversion with DPI-correct scaling --
function canvasToSvg(canvas, dpi) {
  const ctx = canvas.getContext('2d');
  const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
  const svgString = ImageTracer.imagedataToSVG(imageData, SVG_TRACE_OPTIONS);

  // Fix the SVG dimensions to reflect real-world size.
  // SVG CSS px = 96 DPI, so convert from PSD pixels to SVG px.
  const svgWidthPx = (canvas.width / dpi) * 96;
  const svgHeightPx = (canvas.height / dpi) * 96;

  const parser = new DOMParser();
  const doc = parser.parseFromString(svgString, 'image/svg+xml');
  const svg = doc.documentElement;
  svg.setAttribute('width', `${svgWidthPx}`);
  svg.setAttribute('height', `${svgHeightPx}`);
  svg.setAttribute('viewBox', `0 0 ${canvas.width} ${canvas.height}`);

  // Remove white background paths generated by the tracer. It quantizes
  // transparent pixels to white and draws a full-size fill for them.
  const WHITE = /^(?:rgb\(\s*255\s*,\s*255\s*,\s*255\s*\)|#fff(?:fff)?|white)$/i;
  for (const path of [...svg.querySelectorAll('path')]) {
    if (WHITE.test((path.getAttribute('fill') || '').trim())) {
      path.remove();
    }
  }

  return new XMLSerializer().serializeToString(svg);
}

// -- Composite a folder's visible layers onto a full-document-sized canvas --
function compositeFolder(folder, docWidth, docHeight) {
  const canvas = document.createElement('canvas');
  canvas.width = docWidth;
  canvas.height = docHeight;
  const ctx = canvas.getContext('2d');

  for (const child of folder.children || []) {
    if (child.hidden) continue;
    if (child.canvas) {
      ctx.drawImage(child.canvas, child.left || 0, child.top || 0);
    }
  }

  return canvas;
}

// -- Recursive layer tree walk → structured data --
function collectLayers(children, docWidth, docHeight, dpi) {
  if (!children || children.length === 0) return [];

  const layers = [];

  for (const child of children) {
    const isFolder = child.children && child.children.length > 0;

    // For special folders, composite visible layers and trace to SVG.
    if (isFolder && isSpecialFolder(child.name)) {
      const composite = compositeFolder(child, docWidth, docHeight);
      const color = getFolderColor(child.name);
      if (color) colorizeCanvas(composite, ...color);

      if (!isGraphicsFolder(child.name)) {
        const svgHtml = canvasToSvg(composite, dpi);
        const layerColor = color || [128, 128, 128];
        const isBoard = (child.name || '').toLowerCase().includes('board');

        layers.push({
          name: child.name,
          svgHtml,
          color: layerColor,
          type: 'composite',
          isBoard,
          canvas: composite,
        });
      } else {
        // Graphics folders get the canvas but no SVG/draw/STL.
        layers.push({
          name: child.name,
          svgHtml: null,
          color: null,
          type: 'graphics',
          canvas: composite,
        });
      }
    }

    // Include raw layer canvases for display.
    if (child.canvas) {
      layers.push({
        name: child.name || '(unnamed)',
        svgHtml: null,
        color: null,
        type: 'raster',
        hidden: !!child.hidden,
        canvas: child.canvas,
      });
    }

    // Recurse into folder children.
    if (isFolder) {
      const nested = collectLayers(child.children, docWidth, docHeight, dpi);
      // Attach nested layers as children for tree rendering.
      const folderEntry = layers.find(l => l.name === child.name && (l.type === 'composite' || l.type === 'graphics'));
      if (folderEntry) {
        folderEntry.children = nested;
      } else {
        // Non-special folder: just add a folder marker with children.
        layers.push({
          name: child.name || '(unnamed)',
          svgHtml: null,
          color: null,
          type: 'folder',
          children: nested,
        });
      }
    }
  }

  return layers;
}

/**
 * Parse a PSD file and return structured layer data.
 * @param {ArrayBuffer} arrayBuffer - The PSD file contents
 * @param {string} filename - Original filename
 * @returns {{ filename, heading, dpi, layers }} Parsed PSD data
 */
export function handlePsdFile(arrayBuffer, filename) {
  const psd = readPsd(new Uint8Array(arrayBuffer));
  const dpi = psd.imageResources?.resolutionInfo?.horizontalResolution || 72;

  const layers = collectLayers(psd.children, psd.width, psd.height, dpi);

  return {
    filename,
    heading: `${psd.name || 'Untitled'} (${dpi} DPI)`,
    dpi,
    layers,
  };
}
